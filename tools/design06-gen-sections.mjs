// Stage 2 of componentization: split the single design06 baseline into 9 per-section
// React components — each renders its exact <section> element (markup verbatim from the
// captured baseline, nothing changed). The inner HTML is kept in a generated .inner.html
// file imported via ?raw; the <section> tag's own attrs are reproduced as JSX so the DOM
// structure stays byte-identical (no wrapper nodes). Re-run after design06-extract.mjs.
//
// Run with node 22:  node tools/design06-gen-sections.mjs

import { readFile, writeFile, mkdir } from "node:fs/promises";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const ROOT = join(dirname(fileURLToPath(import.meta.url)), "..");
const GEN = join(ROOT, "src", "design06", "_generated");
const SECT_OUT = join(ROOT, "src", "design06", "sections");

// id → component (order is the page order from sections.json)
const NAMES = {
  PBbM6hRVrsx6MjPz: { name: "Hero", title: "The wedding of · Felix & Angel" },
  PBtLyKJDZDgGk7P1: { name: "Calendar", title: "Happily Ever After · calendar · Location · countdown" },
  PBGrcDNxzKvrxrJt: { name: "Timeline", title: "Wedding timeline" },
  PBsHbr4J9zLLY08q: { name: "Details", title: "Wedding Details · Accommodation · Transportation" },
  PBL8ZPfjvBzXjMPd: { name: "Attire", title: "Attire Guide" },
  PB09PH75zdFrcMmz: { name: "Gift", title: "Wedding Gift" },
  PBYbv3X7MfRLX7B4: { name: "Journey", title: "A journey of love" },
  PBJyC0fbP0ThXKGC: { name: "Rsvp", title: "RSVP" },
  PB9GyzXqcqH056Yr: { name: "Closing", title: "Felix & Angel" },
};

const camel = (k) => k.replace(/-([a-z])/g, (_, c) => c.toUpperCase());

// parse a tag's attribute string into a JSX attribute string + style object literal
function attrsToJsx(attrStr) {
  const out = [];
  const re = /([a-zA-Z-]+)="([^"]*)"/g;
  let m;
  while ((m = re.exec(attrStr))) {
    const [, k, v] = m;
    if (k === "class") out.push(`className=${JSON.stringify(v)}`);
    else if (k === "style") {
      const obj = {};
      for (const decl of v.split(";")) {
        const i = decl.indexOf(":");
        if (i === -1) continue;
        const prop = decl.slice(0, i).trim();
        const val = decl.slice(i + 1).trim();
        if (prop) obj[camel(prop)] = val;
      }
      out.push(`style={${JSON.stringify(obj)}}`);
    } else out.push(`${k}=${JSON.stringify(v)}`);
  }
  return out.join(" ");
}

const run = async () => {
  const ids = JSON.parse(await readFile(join(GEN, "sections.json"), "utf8"));
  await mkdir(SECT_OUT, { recursive: true });

  const exports = [];
  for (const id of ids) {
    const meta = NAMES[id] || { name: `Section_${id}`, title: id };
    const full = await readFile(join(GEN, "sections", `${id}.html`), "utf8");
    const m = full.match(/^<section\b([^>]*)>([\s\S]*)<\/section>\s*$/);
    if (!m) throw new Error(`section tag not matched for ${id}`);
    const jsxAttrs = attrsToJsx(m[1]);
    const inner = m[2];

    await writeFile(join(GEN, "sections", `${id}.inner.html`), inner);

    const comp = `// design06 section — ${meta.title} (Canva id ${id})
// Stage 2: structural split only. Markup is verbatim from the captured baseline;
// inner HTML lives in _generated and is injected as-is. No style/text changes.
import inner from "../_generated/sections/${id}.inner.html?raw";

export default function ${meta.name}() {
  return <section ${jsxAttrs} dangerouslySetInnerHTML={{ __html: inner }} />;
}
`;
    await writeFile(join(SECT_OUT, `${meta.name}.tsx`), comp);
    exports.push(meta.name);
  }

  const index =
    `// design06 baseline sections, in page order. Generated by tools/design06-gen-sections.mjs.\n` +
    exports.map((n) => `import ${n} from "./${n}";`).join("\n") +
    `\n\nexport const DESIGN06_SECTIONS = [${exports.join(", ")}] as const;\n`;
  await writeFile(join(SECT_OUT, "index.ts"), index);

  console.log(`✓ generated ${exports.length} section components:`, exports.join(", "));
};

run().catch((e) => {
  console.error(e);
  process.exit(1);
});
